<script>
var mainWrapper = document.querySelector('.main-wrapper');
var menu_side = document.getElementById('btn-toggle');
var sidebar_wrapper = document.getElementById('sidebar-wrapper');

// Add a click event listener to the button
menu_side.addEventListener('click', function() {
    if (sidebar_wrapper.style.display === 'none' || sidebar_wrapper.style.display === '') {
        // Show the sidebar and add the margin to mainWrapper
        sidebar_wrapper.style.display = 'block';
        mainWrapper.classList.add('margin-right-260');  // Add the CSS class to change margin
    } else {
        // Hide the sidebar and remove the margin from mainWrapper
        sidebar_wrapper.style.display = 'none';
        mainWrapper.classList.remove('margin-right-260');  // Remove the CSS class to reset margin
        mainWrapper.classList.add('margin-right-0');  // Remove the CSS class to reset margin
    }
});

</script>